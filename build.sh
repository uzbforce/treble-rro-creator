#!/bin/bash
# =============================================================================
# Treble Overlay Build Script
# =============================================================================
# Reads config.env for device-specific settings, then:
#   1. Backs up current config/vendor/sepolicy to YOUR BACKUPS/
#   2. Generates AndroidManifest.xml files from config.env
#   3. Generates resource XML (res/values/generated.xml) from config.env
#   4. Compiles overlay resources with aapt2
#   5. Zipaligns and signs the APKs
#   6. Auto-generates customize.sh and service.sh from config.env + vendor/
#   7. Packages a KSU/Magisk flashable module
#
# If config.env doesn't exist, the script will:
#   1. Ask if you want to use config.env.example (if found) for testing
#   2. If yes, copy it and proceed with the build
#   3. If no, generate a placeholder config and exit for you to edit
#
# Usage: ./build.sh
#
# Requirements: aapt2, zipalign, apksigner, framework-res.apk
#
# Quick setup:
#   ./setup.sh                    # Auto-downloads/installs everything
#
# Or manually (Debian/Ubuntu):
#   sudo apt install aapt android-sdk-build-tools apksigner android-framework-res
#
# Or manually (other distros):
#   See vendor_extraction_guide.md for platform-specific instructions
# =============================================================================

set -e
set -o pipefail
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
cd "$SCRIPT_DIR"

# ── Local tools directory support ──────────────────────────────────────────
# If tools/ exists, prepend to PATH so aapt2, zipalign, apksigner are found.
# This allows setup.sh to install tools locally without sudo.
if [ -d "${SCRIPT_DIR}/tools" ]; then
    export PATH="${SCRIPT_DIR}/tools:${PATH}"
fi

# Colors
RED='\033[0;31m'; GREEN='\033[0;32m'; YELLOW='\033[1;33m'; BOLD='\033[1m'; NC='\033[0m'
ok()   { echo -e "  ${GREEN}✓${NC} $*"; }
info() { echo -e "  ${YELLOW}→${NC} $*"; }
err()  { echo -e "  ${RED}✗${NC} $*" >&2; }
step() { echo -e "\n${BOLD}━━━ $* ━━━${NC}"; }

# ---------------------------------------------------------------------------
# Backup helper
# ---------------------------------------------------------------------------
BACKUP_DIR="YOUR BACKUPS"
do_backup() {
    mkdir -p "$BACKUP_DIR"
    local backed_up=false
    [ -f "config.env" ]            && { cp config.env "$BACKUP_DIR/config.env";            ok "Backed up config.env";            backed_up=true; }
    [ -f "sepolicy.rule" ]         && { cp sepolicy.rule "$BACKUP_DIR/sepolicy.rule";      ok "Backed up sepolicy.rule";      backed_up=true; }
    [ -d "system/vendor" ]         && { rm -rf "$BACKUP_DIR/vendor"; cp -r system/vendor "$BACKUP_DIR/vendor"; ok "Backed up system/vendor/"; backed_up=true; }
    [ -f "customize.sh" ]          && { cp customize.sh "$BACKUP_DIR/customize.sh";        ok "Backed up customize.sh";       backed_up=true; }
    [ -f "service.sh" ]            && { cp service.sh "$BACKUP_DIR/service.sh";            ok "Backed up service.sh";         backed_up=true; }
    echo ""
    $backed_up && info "Backup saved to ${BACKUP_DIR}/" || info "Nothing to back up"
}

# ---------------------------------------------------------------------------
# Resource XML auto-generator — applies config.env values to overlay resources
# Generates res/values/generated.xml which overrides config.xml/bools.xml/dimens.xml
# ---------------------------------------------------------------------------
generate_resource_xml() {
    local gen="res/values/generated.xml"

    cat > "$gen" << 'GENEOF'
<?xml version="1.0" encoding="utf-8"?>
<!--
    Auto-generated by build.sh from config.env — DO NOT EDIT BY HAND.
    Edit config.env and re-build. Overrides values in config.xml, bools.xml,
    and dimens.xml based on your config.env settings.
-->
<resources>

GENEOF

    # ── Feature flags (bools) ────────────────────────────────────────────
    if [ "$HAS_UDFPS" = "true" ]; then
        cat >> "$gen" << GENEOF
    <bool name="config_supportsUdfps">true</bool>
    <bool name="config_supportsScreenOffUdfps">true</bool>

GENEOF
    fi

    if [ "$HAS_AOD" = "true" ]; then
        cat >> "$gen" << GENEOF
    <bool name="config_dozeAlwaysOnDisplayAvailable">true</bool>

GENEOF
    fi

    if [ "$HAS_DOZE" = "true" ]; then
        cat >> "$gen" << GENEOF
    <bool name="config_dozeAfterScreenOffByDefault">true</bool>
    <bool name="config_dozePulsePickup">true</bool>

GENEOF
    fi

    if [ "$HAS_AUTO_BRIGHTNESS" = "true" ]; then
        cat >> "$gen" << GENEOF
    <bool name="config_automatic_brightness_available">true</bool>

GENEOF
    fi

    if [ "$HAS_BURN_IN_PROTECTION" = "true" ]; then
        cat >> "$gen" << GENEOF
    <bool name="config_enableBurnInProtection">true</bool>
    <bool name="config_statusBarBurnInProtection">true</bool>

GENEOF
    fi

    if [ "$HAS_DOUBLE_TAP_WAKE" = "true" ]; then
        cat >> "$gen" << GENEOF
    <bool name="config_supportDoubleTapWake">true</bool>

GENEOF
    if [ "$HAS_HBM" = "true" ]; then
        cat >> "$gen" << GENEOF
    <bool name="config_dynamic_automatic_brightness_available">true</bool>
    <integer name="config_screenBrightnessExtendedMaximum">${BRIGHTNESS_EXTENDED_MAXIMUM}</integer>

GENEOF
    fi

    fi

    if [ "$HAS_5G" = "true" ]; then
        cat >> "$gen" << GENEOF
    <bool name="config_telephony5gNonStandalone">true</bool>

GENEOF
    fi

    if [ "$HAS_VOLTE" = "true" ]; then
        cat >> "$gen" << GENEOF
    <bool name="config_device_volte_available">true</bool>
    <bool name="config_device_vt_available">true</bool>
    <bool name="config_device_wfc_ims_available">true</bool>

GENEOF
    fi

    # ── Corner radius (dimen) ────────────────────────────────────────────
    cat >> "$gen" << GENEOF
    <dimen name="rounded_corner_radius">${ROUNDED_CORNER_RADIUS}.0px</dimen>
    <dimen name="rounded_corner_radius_top">${ROUNDED_CORNER_RADIUS}.0px</dimen>
    <dimen name="rounded_corner_radius_bottom">${ROUNDED_CORNER_RADIUS}.0px</dimen>
    <dimen name="secondary_rounded_corner_radius">${ROUNDED_CORNER_RADIUS}.0px</dimen>
    <dimen name="secondary_rounded_corner_radius_top">${ROUNDED_CORNER_RADIUS}.0px</dimen>
    <dimen name="secondary_rounded_corner_radius_bottom">${ROUNDED_CORNER_RADIUS}.0px</dimen>
    <dimen name="rounded_corner_content_padding">0px</dimen>

GENEOF

    # ── Status bar height (dimen) ────────────────────────────────────────
    cat >> "$gen" << GENEOF
    <dimen name="status_bar_height">${STATUS_BAR_HEIGHT}.0px</dimen>
    <dimen name="status_bar_height_portrait">${STATUS_BAR_HEIGHT}.0px</dimen>

GENEOF

    # ── Light sensor type (string) ───────────────────────────────────────
    cat >> "$gen" << GENEOF
    <string name="config_displayLightSensorType" translatable="false">${LIGHT_SENSOR_TYPE}</string>

GENEOF

    # ── Brightness (integers + float items) ──────────────────────────────
    cat >> "$gen" << GENEOF
    <integer name="config_screenBrightnessDim">${BRIGHTNESS_DIM}</integer>
    <integer name="config_screenBrightnessDark">${BRIGHTNESS_DARK}</integer>
    <integer name="config_screenBrightnessDoze">${BRIGHTNESS_DOZE}</integer>
    <integer name="config_screenBrightnessSettingMinimum">${BRIGHTNESS_MIN}</integer>
    <integer name="config_screenBrightnessSettingDefault">${BRIGHTNESS_DEFAULT}</integer>
    <integer name="config_screenBrightnessSettingMaximum">${BRIGHTNESS_MAX}</integer>

GENEOF

    # Compute float equivalents (approximate)
    local doze_float=$(echo "scale=8; $BRIGHTNESS_DOZE / 255" | bc 2>/dev/null || echo "0.23529412")
    local dim_float=$(echo "scale=8; $BRIGHTNESS_DIM / 255" | bc 2>/dev/null || echo "0.050980393")
    local min_float=$(echo "scale=8; $BRIGHTNESS_MIN / 255" | bc 2>/dev/null || echo "0.0")
    local default_float=$(echo "scale=8; $BRIGHTNESS_DEFAULT / 255" | bc 2>/dev/null || echo "0.5019608")
    local max_float=$(echo "scale=8; $BRIGHTNESS_MAX / 255" | bc 2>/dev/null || echo "1.0")

    cat >> "$gen" << GENEOF
    <item name="config_screenBrightnessDozeFloat" format="float" type="dimen">${doze_float}</item>
    <item name="config_screenBrightnessDimFloat" format="float" type="dimen">${dim_float}</item>
    <item name="config_screenBrightnessSettingMinimumFloat" format="float" type="dimen">${min_float}</item>
    <item name="config_screenBrightnessSettingDefaultFloat" format="float" type="dimen">${default_float}</item>
    <item name="config_screenBrightnessSettingMaximumFloat" format="float" type="dimen">${max_float}</item>

GENEOF

    # ── Refresh rate (integers) ──────────────────────────────────────────
    cat >> "$gen" << GENEOF
    <integer name="config_defaultRefreshRate">${DEFAULT_REFRESH_RATE}</integer>
    <integer name="config_defaultPeakRefreshRate">${PEAK_REFRESH_RATE}</integer>

GENEOF

    # ── UDFPS sensor position (integer-array) ────────────────────────────
    if [ "$HAS_UDFPS" = "true" ]; then
        cat >> "$gen" << GENEOF
    <integer-array name="config_udfps_sensor_props">
        <item>${UDFPS_X}</item>
        <item>${UDFPS_Y}</item>
        <item>${UDFPS_RADIUS}</item>
    </integer-array>

GENEOF
    fi

    # Close resources
    echo "</resources>" >> "$gen"

    local val_count=$(grep -c '<\(bool\|dimen\|integer\|string\|item\|integer-array\) ' "$gen" 2>/dev/null || echo "0")
    ok "Generated: res/values/generated.xml (${val_count} resource values from config.env)"
}

# ---------------------------------------------------------------------------
# SystemUI overlay resource generator
# Generates systemui_overlay/res/values/generated.xml for doze/AOD behavior
# ---------------------------------------------------------------------------
generate_systemui_resource_xml() {
    local gen="systemui_overlay/res/values/generated.xml"
    mkdir -p "$(dirname "$gen")"

    # doze_display_state_supported: true if HAS_AOD, else false
    local doze_state
    local doze_suspend_state

    if [ "$HAS_AOD" = "true" ]; then
        doze_state="true"
    else
        doze_state="false"
    fi

    # doze_suspend_display_state_supported: false if DOZE_SUSPEND is broken
    # (Samsung hwcomposer issue). Set DOZE_SUSPEND_UNSUPPORTED=true in config.env
    if [ "$DOZE_SUSPEND_UNSUPPORTED" = "true" ]; then
        doze_suspend_state="false"
    else
        doze_suspend_state="true"
    fi

    cat > "$gen" << GENEOF
<?xml version="1.0" encoding="utf-8"?>
<!-- Auto-generated by build.sh from config.env — DO NOT EDIT BY HAND. -->
<resources>
    <bool name="doze_display_state_supported">${doze_state}</bool>
    <bool name="doze_suspend_display_state_supported">${doze_suspend_state}</bool>
    <bool name="doze_proximity_check_before_pulse">false</bool>
    <bool name="doze_pickup_performs_proximity_check">false</bool>
</resources>
GENEOF

    local sysui_count=$(grep -c '<bool ' "$gen" 2>/dev/null || echo "0")
    ok "Generated: systemui_overlay/res/values/generated.xml (${sysui_count} resource values)"
}

# ---------------------------------------------------------------------------
# Service.sh auto-generator — creates post-boot init script from config.env
# ---------------------------------------------------------------------------
generate_service_sh() {
    local target="$1"

    cat > "$target/service.sh" << 'SERVEOF'
#!/sbin/sh
# =============================================================================
# Treble Overlay — Post-Boot Initialization Script
# =============================================================================
# Auto-generated by build.sh from config.env.
# Edit config.env and re-build to change these values.
#
# For vendor-specific additions (Samsung IMS, custom HAL startup, etc.):
#   Place a service.sh at the project root with your customizations.
#   build.sh will detect it and use it instead of auto-generating.
#   See service.sh.example for a reference (Samsung Galaxy A90 5G).
# =============================================================================

# ═══════════════════════════════════════════════════════════════════════════
# SECTION 1: Wait for boot
# ═══════════════════════════════════════════════════════════════════════════
SERVEOF

    cat >> "$target/service.sh" << 'SERVEOF'
while [ "$(getprop sys.boot_completed)" != "1" ]; do
    sleep 5
done

# ═══════════════════════════════════════════════════════════════════════════
# SECTION 2: Enable overlays
# ═══════════════════════════════════════════════════════════════════════════
cmd overlay enable @OVERLAY_PACKAGE@ 2>/dev/null || true
cmd overlay enable @OVERLAY_PACKAGE_SYSTEMUI@ 2>/dev/null || true

SERVEOF

    # ── SECTION 3: AOD / Doze ──────────────────────────────────────────────
    if [ "$HAS_AOD" = "true" ] || [ "$HAS_DOZE" = "true" ]; then
        cat >> "$target/service.sh" << 'SERVEOF'
# ═══════════════════════════════════════════════════════════════════════════
# SECTION 3: AOD / Doze configuration
# ═══════════════════════════════════════════════════════════════════════════
setprop persist.sys.overlay.aod true
setprop persist.sys.phh.disable_display_doze_suspend true

SERVEOF
    fi

    # ── SECTION 4: UDFPS properties ─────────────────────────────────────────
    if [ "$HAS_UDFPS" = "true" ]; then
        local udfps_size=$UDFPS_RADIUS
        cat >> "$target/service.sh" << 'SERVEOF'
# ═══════════════════════════════════════════════════════════════════════════
# SECTION 4: UDFPS Fingerprint properties
# ═══════════════════════════════════════════════════════════════════════════
setprop persist.sys.udfps.custom 1
SERVEOF

        cat >> "$target/service.sh" << SERVEOF
setprop persist.sys.udfps.x ${UDFPS_X}
setprop persist.sys.udfps.y ${UDFPS_Y}
setprop persist.sys.udfps.size ${UDFPS_RADIUS}

SERVEOF

        # Samsung-specific fingerprint flag
        if [ "$HAS_SAMSUNG_FINGERPRINT" = "true" ]; then
            cat >> "$target/service.sh" << 'SERVEOF'
setprop persist.sys.phh.samsung_fingerprint 1

SERVEOF
        fi
    fi

    # ── SECTION 5: Vendor HAL services ──────────────────────────────────────
    local has_hal=false
    if [ "$HAS_VENDOR_FINGERPRINT_HAL" = "true" ] || [ "$HAS_VENDOR_VIBRATOR_HAL" = "true" ]; then
        has_hal=true
        cat >> "$target/service.sh" << 'SERVEOF'
# ═══════════════════════════════════════════════════════════════════════════
# SECTION 5: Vendor HAL services
# ═══════════════════════════════════════════════════════════════════════════
# Start vendor HAL services if their binaries exist in the module.
# Add custom HAL starts here or place a service.sh at project root.
# =========================================================================

# Scan for available HAL binaries and start their services.
# Service names are derived from .rc files in system/vendor/etc/init/.
SERVEOF

        if [ "$HAS_VENDOR_FINGERPRINT_HAL" = "true" ]; then
            cat >> "$target/service.sh" << 'SERVEOF'
# --- Fingerprint HAL ---
# Uncomment and adjust the binary path to match YOUR device's fingerprint HAL.
# Reference: /system/vendor/bin/hw/android.hardware.biometrics.fingerprint@2.3-service.samsung
for fp_hal in /system/vendor/bin/hw/android.hardware.biometrics.fingerprint*; do
    if [ -f "$fp_hal" ]; then
        # Try common service names
        for svc in vendor.fps_hal_aosp vendor.fps_hal vendor.fingerprint; do
            /system/bin/start "$svc" 2>/dev/null && break || true
        done
    fi
    break
done 2>/dev/null || true

SERVEOF
        fi

        if [ "$HAS_VENDOR_VIBRATOR_HAL" = "true" ]; then
            cat >> "$target/service.sh" << 'SERVEOF'
# --- Vibrator HAL ---
# Uncomment and adjust for your device's vibrator HAL.
# Reference: /system/vendor/bin/hw/vendor.samsung.hardware.vibrator@2.2-service
for vib_hal in /system/vendor/bin/hw/*vibrat*; do
    if [ -f "$vib_hal" ]; then
        for svc in sec-vibrator-2-2 vendor.vibrator vibrator; do
            /system/bin/start "$svc" 2>/dev/null && break || true
        done
    fi
    break
done 2>/dev/null || true

SERVEOF
        fi
    fi

    # ── SECTION 6: Samsung FOD (Fingerprint on Display) enable ───────────────
    if [ "$HAS_SAMSUNG_FOD" = "true" ]; then
        cat >> "$target/service.sh" << 'SERVEOF'
# ═══════════════════════════════════════════════════════════════════════════
# SECTION 6: Samsung FOD (Fingerprint on Display) enable
# ═══════════════════════════════════════════════════════════════════════════
# Enables FOD mode on the TSP (Touch Screen Panel). The TSP driver lights
# up the sensor area when the fingerprint HAL activates. Without this,
# the sensor area stays dark and UDFPS will not work.

TSP_SYS="/sys/class/sec/tsp/cmd"
if [ -f "$TSP_SYS" ]; then
    chmod 0660 "$TSP_SYS" 2>/dev/null || true
    if echo "fod_enable,1" > "$TSP_SYS" 2>/dev/null; then
        log -t "treble-overlay" "  ✓ FOD enabled via $TSP_SYS" || true
        setprop persist.sys.phh.fod_enabled 1
    else
        log -t "treble-overlay" "  ✗ Failed to write FOD enable (SELinux?)" || true
    fi
else
    log -t "treble-overlay" "  - TSP sysfs not found at $TSP_SYS" || true
fi

SERVEOF
    fi

    # ── SECTION 7: Samsung Vibrator HAL start ────────────────────────────────
    if [ "$HAS_SAMSUNG_VIBRATOR" = "true" ]; then
        cat >> "$target/service.sh" << 'SERVEOF'
# ═══════════════════════════════════════════════════════════════════════════
# SECTION 7: Samsung Vibrator HAL — start existing vendor service
# ═══════════════════════════════════════════════════════════════════════════
# Stock Samsung vendor has vibrator HAL at:
#   /vendor/bin/hw/vendor.samsung.hardware.vibrator@2.2-service
# Service name: sec-vibrator-2-2 (defined in vendor/etc/init/*.rc)

VIB_SERVICE="sec-vibrator-2-2"

if [ -n "$(getprop init.svc.$VIB_SERVICE 2>/dev/null)" ]; then
    if [ "$(getprop init.svc.$VIB_SERVICE)" != "running" ]; then
        log -t "treble-overlay" "  Starting: $VIB_SERVICE" || true
        start $VIB_SERVICE 2>/dev/null || true
        sleep 1
        if [ "$(getprop init.svc.$VIB_SERVICE)" = "running" ]; then
            log -t "treble-overlay" "  ✓ Vibrator HAL started" || true
            setprop persist.sys.phh.vibrator_started 1
        else
            log -t "treble-overlay" "  ✗ Vibrator HAL failed to start" || true
        fi
    else
        log -t "treble-overlay" "  ✓ Vibrator HAL already running" || true
        setprop persist.sys.phh.vibrator_started 1
    fi
fi

SERVEOF
    fi

    # ── SECTION 8: Samsung device node permissions for fingerprint ───────────
    if [ "$HAS_UDFPS" = "true" ]; then
        cat >> "$target/service.sh" << 'SERVEOF'
# ═══════════════════════════════════════════════════════════════════════════
# SECTION 8: Samsung device node permissions for fingerprint
# ═══════════════════════════════════════════════════════════════════════════
# These are normally done by vendor/etc/init/fingerprint_common.rc
# on the `on boot` trigger. On GSIs, we set them here to be safe.

# Samsung ESFP (Exynos Security FingerPrint) device node
if [ -e "/dev/esfp0" ]; then
    chmod 0660 /dev/esfp0 2>/dev/null || true
    chown system system /dev/esfp0 2>/dev/null || true
fi

# Goodix fingerprint device node
if [ -e "/dev/goodix_fp" ]; then
    chmod 0660 /dev/goodix_fp 2>/dev/null || true
    chown system system /dev/goodix_fp 2>/dev/null || true
fi

# Sysfs fingerprint node ownership
FP_SYSFS="/sys/class/fingerprint/fingerprint"
if [ -d "$FP_SYSFS" ]; then
    for node in type_check name vendor adm bfs_values position cbgecnt intcnt resetcnt wuhbtest rb; do
        if [ -f "$FP_SYSFS/$node" ]; then
            chown system radio "$FP_SYSFS/$node" 2>/dev/null || true
        fi
    done
fi

# Data directories for fingerprint template storage
for dir in /data/vendor/misc /data/vendor/misc/qti_fp /data/vendor/misc/qti_fp/bg_estimation /data/vendor/misc/qti_fp/calib_test /data/vendor/misc/qti_fp/template /data/vendor/biometrics /data/vendor/fpSnrTest; do
    [ -d "$dir" ] && chmod 0770 "$dir" 2>/dev/null || true
done

SERVEOF
    fi

    # ── SECTION 6: Connectivity (5G / VoLTE) ────────────────────────────────
    if [ "$HAS_5G" = "true" ] || [ "$HAS_VOLTE" = "true" ]; then
        cat >> "$target/service.sh" << 'SERVEOF'
# ═══════════════════════════════════════════════════════════════════════════
# SECTION 6: Connectivity (5G / VoLTE)
# ═══════════════════════════════════════════════════════════════════════════
SERVEOF

        if [ "$HAS_5G" = "true" ]; then
            cat >> "$target/service.sh" << 'SERVEOF'
setprop persist.sys.phh.force_display_5g 1
setprop persist.sys.phh.radio.nr 1

SERVEOF
        fi

        if [ "$HAS_VOLTE" = "true" ]; then
            cat >> "$target/service.sh" << 'SERVEOF'
setprop persist.dbg.volte_avail_ovr 1
setprop persist.dbg.allow_ims_off 0
settings put global preferred_network_mode 26

SERVEOF
        fi
    fi

    # ── SECTION 7: Brightness / panel-specific fixes ────────────────────────
    # This section is generic — Samsung users should add full_brightness
    # via a custom service.sh at project root or service.sh.example.
    cat >> "$target/service.sh" << 'SERVEOF'
# ═══════════════════════════════════════════════════════════════════════════
# SECTION 7: Device-specific properties
# ═══════════════════════════════════════════════════════════════════════════
# Add vendor-specific setprop calls below.
# For Samsung panels, uncomment:
#   setprop persist.sys.samsung.full_brightness true
#   setprop persist.sys.qcom-brightness -1
# =========================================================================

SERVEOF

    # Close
    chmod +x "$target/service.sh"
    local svc_count=5
    [ "$HAS_UDFPS" = "true" ] && svc_count=$((svc_count + 2))
    [ "$HAS_AOD" = "true" ] && svc_count=$((svc_count + 1))
    [ "$HAS_5G" = "true" ] && svc_count=$((svc_count + 1))
    [ "$HAS_VOLTE" = "true" ] && svc_count=$((svc_count + 1))
    [ "$has_hal" = "true" ] && svc_count=$((svc_count + 1))

    info "service.sh auto-generated (${svc_count} sections)"
}

# ---------------------------------------------------------------------------
# customize.sh auto-generator
# ---------------------------------------------------------------------------
generate_customize_sh() {
    local target="$1"
    cat > "$target/customize.sh" << 'CUSTOMIZE_EOF'
#!/sbin/sh
# =============================================================================
# Treble Overlay — KSU/Magisk Module Install Script
# =============================================================================
# Auto-generated by build.sh from system/vendor/ contents.
# Edit the template in "TEMPLATE RECOVERIES (Don't touch)/customize.sh"
# and place it at project root if you need custom SELinux contexts.
# =============================================================================

MODPATH=${0%/*}

ui_print "  Installing ${MODPATH##*/}..."

# ── Overlay APKs ──────────────────────────────────────────────────────────
set_perm_recursive $MODPATH/system/product/overlay 0 0 0644 u:object_r:system_file:s0

CUSTOMIZE_EOF

    # Vendor HAL binaries
    if [ -d "system/vendor/bin/hw" ] && [ "$(ls -A system/vendor/bin/hw/ 2>/dev/null | grep -c .)" -gt 0 ]; then
        cat >> "$target/customize.sh" << 'CUSTOMIZE_BIN'

# ── Vendor HAL binaries ───────────────────────────────────────────────────
CUSTOMIZE_BIN
        for f in system/vendor/bin/hw/*; do
            [ -f "$f" ] || continue
            local name
            name=$(basename "$f")
            cat >> "$target/customize.sh" << CUSTOMIZE_BIN_ITEM
if [ -f "\\$MODPATH/system/vendor/bin/hw/$name" ]; then
    set_perm \\$MODPATH/system/vendor/bin/hw/$name 0 2000 0755 u:object_r:vendor_file:s0
    ui_print "  • HAL: $name"
fi

CUSTOMIZE_BIN_ITEM
        done
    fi

    # Shared libraries
    if [ -d "system/vendor/lib64" ] && [ "$(ls -A system/vendor/lib64/ 2>/dev/null | grep -c .)" -gt 0 ]; then
        cat >> "$target/customize.sh" << 'CUSTOMIZE_LIB'

# ── Shared libraries ──────────────────────────────────────────────────────
if [ -d "$MODPATH/system/vendor/lib64" ]; then
    set_perm_recursive $MODPATH/system/vendor/lib64 0 0 0644 u:object_r:vendor_file:s0
    ui_print "  • Vendor shared libraries set"
fi

CUSTOMIZE_LIB
    fi

    # Init .rc files
