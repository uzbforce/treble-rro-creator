        info "sepolicy.rule has no allow rules — template only, skipping"
    fi
else
    if [ "$INCLUDE_VENDOR_HALS" = "true" ]; then
        info "sepolicy.rule not found or has no rules — skipping"
    else
        info "sepolicy.rule skipped (overlay-only build)"
    fi
fi

# service.sh — auto-generate or use user's copy at project root
if [ -f service.sh ]; then
    # User placed a service.sh at project root — substitute placeholders
    sed -e "s/@OVERLAY_PACKAGE@/${OVERLAY_PACKAGE}/g" \
        -e "s/@OVERLAY_PACKAGE_SYSTEMUI@/${OVERLAY_PACKAGE_SYSTEMUI}/g" \
        service.sh > "${MODULE_TMP}/service.sh"
    chmod +x "${MODULE_TMP}/service.sh"
    ok "service.sh: using project root's service.sh (package: ${OVERLAY_PACKAGE})"
else
    generate_service_sh "$MODULE_TMP"
    # Substitute placeholders in the generated file
    sed -i -e "s/@OVERLAY_PACKAGE@/${OVERLAY_PACKAGE}/g" \
           -e "s/@OVERLAY_PACKAGE_SYSTEMUI@/${OVERLAY_PACKAGE_SYSTEMUI}/g" \
           "${MODULE_TMP}/service.sh"
    ok "service.sh auto-generated (package: ${OVERLAY_PACKAGE})"
fi

# ---------------------------------------------------------------------------
# Copy vendor HAL files (only if INCLUDE_VENDOR_HALS is set)
# ---------------------------------------------------------------------------
VENDOR_HALS_COPIED=false

if [ "$INCLUDE_VENDOR_HALS" = "true" ]; then
    # Vendor HAL binaries
    if [ -d "system/vendor/bin/hw" ] && [ "$(ls -A system/vendor/bin/hw/ 2>/dev/null | grep -c .)" -gt 0 ]; then
        mkdir -p "${MODULE_TMP}/system/vendor/bin/hw"
        cp system/vendor/bin/hw/* "${MODULE_TMP}/system/vendor/bin/hw/"
        for f in system/vendor/bin/hw/*; do
            ok "Vendor HAL: $(basename "$f")"
        done
        VENDOR_HALS_COPIED=true
    fi

    # Shared libraries
    if [ -d "system/vendor/lib64" ] && [ "$(ls -A system/vendor/lib64/ 2>/dev/null | grep -c .)" -gt 0 ]; then
        mkdir -p "${MODULE_TMP}/system/vendor/lib64"
        cp system/vendor/lib64/*.so "${MODULE_TMP}/system/vendor/lib64/" 2>/dev/null || true
        for f in system/vendor/lib64/*.so; do
            [ -f "$f" ] && ok "Vendor lib: $(basename "$f")"
        done
    fi

    # Init .rc files
    if [ -d "system/vendor/etc/init" ] && [ "$(ls -A system/vendor/etc/init/ 2>/dev/null | grep -c .)" -gt 0 ]; then
        mkdir -p "${MODULE_TMP}/system/vendor/etc/init"
        cp system/vendor/etc/init/* "${MODULE_TMP}/system/vendor/etc/init/"
        ok "Included $(ls system/vendor/etc/init/* 2>/dev/null | wc -l) init .rc files"
    fi

    # VINTF manifest fragments
    if [ -d "system/vendor/etc/vintf/manifest" ] && [ "$(ls -A system/vendor/etc/vintf/manifest/ 2>/dev/null | grep -c .)" -gt 0 ]; then
        mkdir -p "${MODULE_TMP}/system/vendor/etc/vintf/manifest"
        cp system/vendor/etc/vintf/manifest/* "${MODULE_TMP}/system/vendor/etc/vintf/manifest/"
        ok "Included $(ls system/vendor/etc/vintf/manifest/* 2>/dev/null | wc -l) VINTF manifest files"
    fi

    if [ "$VENDOR_HALS_COPIED" = "true" ]; then
        info "Vendor HAL files included in module"
    else
        info "No vendor HAL files found — build pure overlay only"
    fi
else
    info "Vendor HAL files skipped (overlay-only build)"
fi

